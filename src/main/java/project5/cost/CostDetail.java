package project5.cost;

public class CostDetail {
	private int no;
	private String costcontent;
	private String costnote;
	private int costex;
	private int cskey;
	private int take;
	private int prjkey;
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public String getCostcontent() {
		return costcontent;
	}
	public void setCostcontent(String costcontent) {
		this.costcontent = costcontent;
	}
	public String getCostnote() {
		return costnote;
	}
	public void setCostnote(String costnote) {
		this.costnote = costnote;
	}
	public int getCostex() {
		return costex;
	}
	public void setCostex(int costex) {
		this.costex = costex;
	}
	public int getCskey() {
		return cskey;
	}
	public void setCskey(int cskey) {
		this.cskey = cskey;
	}
	public int getTake() {
		return take;
	}
	public void setTake(int take) {
		this.take = take;
	}
	public int getPrjkey() {
		return prjkey;
	}
	public void setPrjkey(int prjkey) {
		this.prjkey = prjkey;
	}
	
}
