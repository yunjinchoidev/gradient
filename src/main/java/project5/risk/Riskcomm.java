package project5.risk;

import java.util.Date;

public class Riskcomm {
	private int cnt;
	private int level;
	private int rcommkey;
	private String commcontent;
	private Date commdate;
	private String scommdate;
	private String name;
	private String id;
	private int refno;
	public int getCnt() {
		return cnt;
	}
	public void setCnt(int cnt) {
		this.cnt = cnt;
	}
	public int getLevel() {
		return level;
	}
	public void setLevel(int level) {
		this.level = level;
	}
	public int getRcommkey() {
		return rcommkey;
	}
	public void setRcommkey(int rcommkey) {
		this.rcommkey = rcommkey;
	}
	public String getCommcontent() {
		return commcontent;
	}
	public void setCommcontent(String commcontent) {
		this.commcontent = commcontent;
	}
	public Date getCommdate() {
		return commdate;
	}
	public void setCommdate(Date commdate) {
		this.commdate = commdate;
	}
	public String getScommdate() {
		return scommdate;
	}
	public void setScommdate(String scommdate) {
		this.scommdate = scommdate;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getRefno() {
		return refno;
	}
	public void setRefno(int refno) {
		this.refno = refno;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	
	
}
